1. download the latest copy of fire form builder from http://www.phpletter.com/DOWNLOAD/
2. unzip the downloaded file and place it to your website root folder
3. create new database (said fireform), run all queries in the firefor.sql file to create the database schema
4. config your db settting in fire.form.setting.php
5. config your email setting in fire.form.setting.php
6. config your website url in config.base.php, ensure your URL_SITE in fire.form.setting.php point to the root of fireform application.
7. done, now you can log into your fire form builder from URL_SITE/admin/login.php  
